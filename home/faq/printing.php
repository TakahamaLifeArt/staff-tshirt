					<h4><span class="red_no">Q</span>デジタル転写プリントとはどんなものですか？</h4>
					<p><span class="blk_no">A</span>レーザーコピーで出力したものに白い色とのりを印刷して、それを熱で転写するものです。簡単に言えばアイロンプリントのようなものです。もし白いTシャツに転写するのであれば白い色を刷らなくて済むので、その分安くなります。
