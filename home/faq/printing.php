					<h4><span class="red_no">Q</span>デザインが細かいのですが　シルクプリントで表現できますか？</h4>
					<p><span class="blk_no">A</span>シルクプリントは実際のデータより太ってプリントされるので細かい柄には向いてません。線の部分は0.5ミリ以上、抜きの場合は1ミリはあった方が良いでしょう。（線とはインクの出るところ。抜きとは線と線の間のプリントされない部分。）</p>
				</div>
				
				<div class="inner">
					<h4><span class="red_no">Q</span>写真をシルクプリント1色で表現したいのですが</h4>
