					<p><span class="blk_no">A</span>縫い目の上など平にならないところの印刷は難しくなります。すべての縫い目から２ｃｍは離れた場所に印刷した方がきれいに仕上がります。どうしてもしたいというご要望があればいたしますが、きれいには仕上がりません。</p>
